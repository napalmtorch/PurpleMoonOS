    public abstract class Font
    {
        public string name;
        public int id;
        public int characterWidth { get; private set; }
        public int characterHeight { get; private set; }
        public List<uint[]> characters = new List<uint[]>();

        // default constructor
        public Font() { name = "null"; id = -1; }

        // constructor
        public Font(string name, int cw, int ch)
        {
            this.name = name;
            this.characterWidth = cw;
            this.characterHeight = ch;
        }

        public int StringWidth(string txt)
        {
            if (txt.Length > 0)
            { return (characterWidth + Graphics2D.FONT_SPACING) * txt.Length; }
            else { return 0; }
        }
    }

